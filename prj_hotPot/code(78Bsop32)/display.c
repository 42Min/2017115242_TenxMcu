//==========================定义LED灯点亮函数==================================
void LedOn() {
		//打开LED等并延时1000毫秒
    F_ledOn();
    Delay(1000);
}
//=============================================================================

//==========================定义LED灯关灭函数==================================
void LedOff() {
		//关闭LED等并延时1000毫秒
    F_ledOff();
    Delay(1000);
}
//=============================================================================

//==========================定义LED灯闪烁函数==================================
void LedFlash() {
		//打开LED等并延时1000毫秒
    F_ledOn();
    Delay(1000);

    //关闭LED等并延时1000毫秒
    F_ledOff();
    Delay(1000);
}
//=============================================================================

